        private void carregaTabela(){
        try {
                DisciplinaOfertadaDAO dao = new DisciplinaOfertadaDAO();
                listaBeans = dao.consultarDescricao(txtPesquisar.getText());        
                Vector linhas = new Vector();
                Vector colunas = new Vector();                
                colunas.add("Sigla");
                colunas.add("NÃºmero");
                
                for (DisciplinaOfertada bean : listaBeans) {
                    Vector linha = new Vector();
                    linha.add(bean.getDissigla());
                    linha.add(bean.getNumero());
                    linhas.add(linha);
                }
                          
                DefaultTableModel model = new DefaultTableModel(linhas, colunas);
                tabela.setModel(model);
            } catch (Exception ex) {
                System.out.println("Erro carregar tabela: "+ex.getMessage());
            }
    }
        
